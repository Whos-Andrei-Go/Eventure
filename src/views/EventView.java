    private void updateDetails(Event daEvent){
        hdrMain.setText(daEvent.getName());
        lblLocation.setText("Event Location: " + daEvent.getLocation());
        lblStartDate.setText("Event Start Date: " + daEvent.getFormattedStartTime());
        lblEndDate.setText("Event End Date: " + daEvent.getFormattedEndTime());
        lblTicketPrice.setText("");
        lblQuantityLeft.setText("");
        txtDescription.setText(daEvent.getDescription());
        
        cmbTicketType.removeAllItems();
        
        ticketTypes = ticketController.getTicketTypesByEventId(daEvent.getId());

        for (TicketType ticket : ticketTypes) {
            cmbTicketType.addItem(ticket.getName()); // Add the name of each ticket type
        }
    }
