    private void cmbTicketTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTicketTypeActionPerformed
        // TODO add your handling code here:
        String selectedTicketName = (String) cmbTicketType.getSelectedItem();

        // Find the selected TicketType object based on the name
        TicketType selectedTicketType = null;
        
        if (ticketTypes != null){
            for (TicketType ticket : ticketTypes) {
                if (ticket.getName().equals(selectedTicketName)) {
                    selectedTicketType = ticket;
                    break;
                }
            }
        }
       
        // If a valid ticket type is found, update the price label
        if (selectedTicketType != null) {
            BigDecimal price = selectedTicketType.getPrice();
            int quantityLeft = selectedTicketType.getQuantity();
            lblTicketPrice.setText("PHP " + String.format("%.2f", price)); // Update label with price in PHP
            lblQuantityLeft.setText(quantityLeft + " tickets left");
        }
    }//GEN-LAST:event_cmbTicketTypeActionPerformed

